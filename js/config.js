/* data.json 한 번만 fetch해서 캐시 */
async function loadDataJson() {
  if(_dataCache) return _dataCache;
  try {
    var res = await fetch('./data.json?t=' + Date.now());
    _dataCache = await res.json();
    // 섹션1 업데이트 시점 표시
    if (_dataCache && _dataCache.kospi) {
      var lastKospi = _dataCache.kospi[_dataCache.kospi.length - 1];
      if (lastKospi) {
        var ym = lastKospi.ym;
        var dateStr = ym.length === 8
          ? ym.substring(0,4)+'.'+ym.substring(4,6)+'.'+ym.substring(6,8)
          : ym.substring(0,4)+'.'+ym.substring(4,6);
        var el = document.getElementById('data-updated');
        if (el) el.textContent = '최신 ' + dateStr + ' 기준';
      }
    }
    return _dataCache;
  } catch(e) { return null; }
}

